<h1> pigtex </h1>

<p>
    PigTeX is a simple scripting language that is translated into LaTeX at 
    compile time, allowing for a much faster and more streamlined typesetting
    experience. Eventually, it will also come with its own tooling to help with
    compiling.
</p>

<p>
    As a physicist and mathematician, I write many 
    thousands of lines of LaTeX, and I'm pretty sick of the clunky and archaic
    syntax. The plan is to create a simplified language that only implements
    what's required for a good <em>mathematics</em> paper: the
    <code>equation</code>, <code>flalign</code> and <code>flalign*</code>
    environments, inline and block math environments (usually delimited by 
    <code>$</code> and <code>$$</code>), and a few others. This project is a
    dictatorship, not a democracy: I will probably only implement what I
    personally use when writing math papers. This will more than likely be 
    enough for 90% of use cases in mathematics; if more functionality is 
    required that isn't part of PigTeX, I recommend using full LaTeX.
</p>

<p>
    This project is in its infancy and doesn't even have a repository yet. When
    I make any significant progress, I will update this page and push the code
    to a public repo. Stay tuned!
</p>
