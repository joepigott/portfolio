<h2 id="physics">
    <a href="#physics">physics</a>
</h2>

<p>
    As of May 2023, I've completed the introductory courses (including their
    labs), as well as <em>Computer Methods in Physics</em>, both <em>Intro. 
    to Modern Physics</em> and <em>Intermediate Modern Physics</em>, and 
    <em>Intermediate Classical Mechanics</em>. Next semester, I'm registered
    for <em>Electromagnetism I</em>, <em>Foundations of Wave Phenomena</em>, 
    and <em>Intermediate Physics Laboratory</em>.
</p>

<p>
    The most interesting class I've taken so far is <em>Intermediate Classical
    Mechanics</em>. We started by forgetting everything we knew about physics,
    then rederived the Newtonian framework from first principles. By going
    about teaching this way, it helped cement the ideas in reality. After this,
    we defined energy (again from first principles), and used this definition
    to reform the Lagrangian framework. After this, we studied in-depth
    oscillations, both damped and undamped. We then finished the semester by 
    forming the Hamiltonian framework by examining certain properties of the 
    Lagrangian.
</p>

<p>
    This area of physics is where I'm currently the most experienced, and also
    where my interests lie. I'm sure that this will be the case for each physics
    course I take from this point forward (e.g. electromagnetism), but there's just
    something so beautiful about classical mechanics and the way the solutions work
    out.
</p>

<h2 id="mathematics">
    <a href="#mathematics">mathematics</a>
</h2>

<p>
    As of May 2023, I've completed both <em> Calculus I</em> and <em>Calculus 
    II</em>, <em>Multivariable Calculus</em>, <em>Linear Algebra</em>, 
    <em>Ordinary Differential Equations</em>, and <em>Partial Differential 
    Equations</em>. I'm planning on taking <em>Complex Variables</em> in Spring
    2024 to complete the mathematics minor, as well as <em>Theory of Linear 
    Algebra</em> at some point to prepare myself for quantum mechanics.
</p>

<p>
    By far the most interesting math class I've taken is <em>Partial
    Differential Equations</em>. The class used <em>Applied Partial
    Differential Equations</em> by Logan for the text, and the first month or
    so was devoted to deriving the advection, diffusion, and wave equations 
    from a physical basis. The course covered the method of characteristics, 
    method of reflection, and Laplace and Fourier transforms for problems on
    unbounded domains, and also covered Fourier expansions and Sturm-Lioville
    Theory for problems on bounded domains. I gained exceptional progress in
    understanding and solving Fourier series.
</p>
