<h1>linux</h1>

<img src={screenshot} alt="my custom linux setup" style="border-radius: 0.5em; width: 100%; max-width: 550px; align-self: center;"/>

<p>
    I've been using Arch Linux (btw) as my daily operating system for a year
    now. I used Windows 10 and 11 for years prior, but I got so tired of the
    telemetry and bloatware that I backed up my files and wiped the
    installation. My frustration with bloat is what pushed me to Arch: it's
    simple, easy to install, and comes with absolutely nothing I don't want.
</p>

<p>
    After wiping Windows, I reinstalled it purely for gaming purposes; I use 
    Linux for absolutely every other task.
</p>

<h2 id="the-rice"><a href="#the-rice">the rice</a></h2>

<p>
    I use <a href="https://awesomewm.org">awesomewm</a> as my window manager.
    It's fully configurable and provides an excellent toolkit for windowing and
    widgets.
</p>

<p>
    The status bar is currently <a href="https://polybar.github.io">polybar</a>
    but this will most likely be replaced with wibar, which is provided
    natively by awesomewm.
</p>

<p>
    I use <a href="https://neovim.io">neovim</a> as an editor and will never
    use anything else. The entire editor has been customized and configured to
    my liking, over the course of far too many hours.
</p>

<p>
    There are countless other tools that I won't list here. The full
    configuration can be found in my 
    <a href="https://github.com/joepigott/dots">dotfiles repo</a>.
</p>

<h2 id="the-server"><a href="#the-server">the server</a></h2>

